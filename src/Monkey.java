public class Monkey extends Animal{

    public void eat() {
        System.out.println("The monkey is eating.");
    }

    public void run() {
        System.out.println("The monkey is running.");
    }

    public void swim() {
        System.out.println("The monkey is swimming.");
    }

    public void jump() {
        System.out.println("The monkey is jumping.");
    }
}
