public class Lion extends Animal{

    public void eat() {
        System.out.println("The lion is eating.");
    }

    public void run() {
        System.out.println("The lion is running.");
    }

    public void swim() {
        System.out.println("The lion is swimming.");
    }

    public void jump() {
        System.out.println("The lion is jumping.");
    }

    public void roar() {
        System.out.println("The lion is roaring.");
    }
}
