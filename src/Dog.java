public class Dog extends Animal{

    public void eat() {
        System.out.println("The dog is eating.");
    }

    public void run() {
        System.out.println("The dog is running.");
    }

    public void swim() {
        System.out.println("The dog is swimming.");
    }

    public void jump() {
        System.out.println("The dog is jumping.");
    }
}
