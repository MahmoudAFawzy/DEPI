public class Horse extends Animal {


    public void eat() {
        System.out.println("The horse is eating.");
    }

    public void run() {
        System.out.println("The horse is running.");
    }

    public void swim() {
        System.out.println("The horse is swimming.");
    }

    public void jump() {
        System.out.println("The horse is jumping.");
    }
}
