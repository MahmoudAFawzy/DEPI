public class Cat extends Animal{

    public void eat() {
        System.out.println("The cat is eating.");
    }

    public void run() {
        System.out.println("The cat is running.");
    }

    public void swim() {
        System.out.println("The cat is swimming.");
    }

    public void jump() {
        System.out.println("The cat is jumping.");
    }
}
