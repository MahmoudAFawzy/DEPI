public class Animal {


    protected void eat() {
        System.out.println("The animal is eating.");
    }

    protected void run() {
        System.out.println("The animal is running.");
    }

    protected void swim() {
        System.out.println("The animal is swimming.");
    }

    protected void jump() {
        System.out.println("The animal is jumping.");
    }

    protected void roar() {
        System.out.println("The animal is roaring.");
    }
}
